// Hook for using translations in components
export const useTranslation = (userLanguage = 'he') => {
  const t = (key, params = {}) => {
    let translation = translations[userLanguage]?.[key] || translations.he[key] || key;
    
    // Replace parameters like {name} with actual values
    Object.keys(params).forEach(param => {
      translation = translation.replace(`{${param}}`, params[param]);
    });
    
    return translation;
  };
  
  const dir = userLanguage === 'he' ? 'rtl' : 'ltr';
  const align = userLanguage === 'he' ? 'right' : 'left';
  
  return { t, dir, align, language: userLanguage };
};

export default translations;